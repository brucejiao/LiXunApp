public class MineDraftListBean {
    private String draftTime;
    private String draftHeaderImg;
    private String draftTitle;
    private String draftContent;
    private String draftPrice;
    private String draftEditBtn;

    public String getDraftTime() {
        return draftTime;
    }

    public void setDraftTime(String draftTime) {
        this.draftTime = draftTime;
    }

    public String getDraftHeaderImg() {
        return draftHeaderImg;
    }

    public void setDraftHeaderImg(String draftHeaderImg) {
        this.draftHeaderImg = draftHeaderImg;
    }

    public String getDraftTitle() {
        return draftTitle;
    }

    public void setDraftTitle(String draftTitle) {
        this.draftTitle = draftTitle;
    }

    public String getDraftContent() {
        return draftContent;
    }

    public void setDraftContent(String draftContent) {
        this.draftContent = draftContent;
    }

    public String getDraftPrice() {
        return draftPrice;
    }

    public void setDraftPrice(String draftPrice) {
        this.draftPrice = draftPrice;
    }

    public String getDraftEditBtn() {
        return draftEditBtn;
    }

    public void setDraftEditBtn(String draftEditBtn) {
        this.draftEditBtn = draftEditBtn;
    }
}
